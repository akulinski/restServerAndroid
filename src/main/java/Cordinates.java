public class Cordinates {

    String cordinatesx;
    String cordinatesy;

    Cordinates(String cordinatesx,String cordinatesy){
        cordinatesx=cordinatesx;
        cordinatesy=cordinatesy;
    }


    public String getX( )
    {
        return cordinatesx;
    }

    public String getY( )
    {
        return cordinatesy;
    }

}
